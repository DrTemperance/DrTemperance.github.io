var likes1 = 0;

function likePhoto1() {
  var likeCount1 = document.getElementById("likeCount1");
  if (likes1 != 1) {
    likes1 += 1;
  } else {
    likes1 -= 1;
  }
  likeCount1.innerHTML = likes1;
}
